// CONCATENATION:

console.log(1 + 2 + '3'); // 33
console.log(1 + '2' + 3); // 123
console.log('1' + 2 + 3); // 123

console.log(1 + 2 + +'3'); // 6
console.log(1 + +'2' + 3); // 6
console.log(+'1' + 2 + 3); // 6

