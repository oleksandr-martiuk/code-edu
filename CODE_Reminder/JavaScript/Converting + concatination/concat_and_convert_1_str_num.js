// CONVERTING:

console.log(1 - '2'); // -1
console.log('1' - 2); // -1

//--------------------------

console.log(1 / '2'); // 0.5
console.log('1' / 2); // 0.5

//--------------------------

console.log(1 * '2'); // 2
console.log('1' * 2); // 2
